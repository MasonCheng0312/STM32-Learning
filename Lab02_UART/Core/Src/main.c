/* USER CODE BEGIN 0 */
void my_UART_init(){
  // UART2 is on APB1 bus(bit 17), enable its clock
    RCC->APB1ENR |= (1 << 17);

    // GPIOA is on AHB1 bus(bit 0), enable its clock
    // from datasheet, UART2 can on PA or PD, we use PA2(TX) and PA3(RX)
    RCC->AHB1ENR |= (1 << 0);

    // Set PA2 and PA3 to alternate function mode (10)
    // 提示：GPIOA->MODER，清除舊設定，填入 AF 模式代碼 (二進位 10)
    GPIOA->MODER &= ~(3 << (2 * 2)); // 先清零 (2*2 代表 Pin 2 的起始 bit)
    GPIOA->MODER |=  (2 << (2 * 2)); // 填入 10 (AF Mode)

    GPIOA->MODER &= ~(3 << (3 * 2)); // 先清零 (3*2 代表 Pin 3 的起始 bit)
    GPIOA->MODER |= (2 << (3 * 2)); // 填入 10 (AF Mode)

    // 因為 PA2, PA3 小於 8，所以使用 AFR[0] (Low Register)
    // 每個 Pin 佔 4 個 bits。
    // Pin 2 的位置在: 2 * 4 = bit 8
    // Pin 3 的位置在: 3 * 4 = bit 12
    // AF7 的值是 0x7 (二進位 0111)

    GPIOA->AFR[0] |= (0x7 << ( 2 * 4)); // 設定 PA2 為 AF7
    GPIOA->AFR[0] |= (0x7 << ( 3 * 4)); // 設定 PA3 為 AF7

    // 設定 UART2 的參數
    USART2->BRR = 0x16D; // 115200 @ 42MHz 誤差0.11%
    USART2->CR1 |= (USART_CR1_UE | USART_CR1_TE | USART_CR1_RE);
}

void my_UART_putc(char c) {
    // 1. 等待：直到 TXE 變成 1
    // 邏輯：只要 SR 的第 7 個 bit 是 0，就卡在 while 迴圈空轉
    // 提示：(USART2->SR & (1 << 7)) 
    // 如果結果是 0，代表 !結果 為真，就會繼續跑迴圈
    
    while ( !(USART2->SR & (1 << 7)) );  

    // 2. 寫入：把字元丟進 Data Register
    // 硬體一看到 DR 有東西，就會自動把它搬去發送室送出去
    USART2->DR = c;
}

int _write(int file, char *ptr, int len)
{
    // 把 ptr 指向的字串，一個一個用我們的函式送出去
    for (int i = 0; i < len; i++)
    {
        my_UART_putc(ptr[i]);
    }
    return len;
}
